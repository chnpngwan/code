package com.nchu.fruit.pojo;

public class SlideConfig{

	private int id;
	private int type;
	private int byId;
	private String picname;
	
	public SlideConfig() {}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getById() {
		return byId;
	}
	public void setById(int byId) {
		this.byId = byId;
	}
	public String getPicname() {
		return picname;
	}
	public void setPicname(String picname) {
		this.picname = picname;
	}
	
	
	 
}
