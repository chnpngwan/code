package com.nchu.fruit.utils;

public class PaginationOther {
	private int page; // 第几页  
	private int rows; // 行数  
	  
	public int getPage() {  
	        return page;  
	    }  
	  
	    public void setPage(int page) {  
	        this.page = page;  
	    }  
	  
	    public int getRows() {  
	        return rows;  
	    }  
	  
	    public void setRows(int rows) {  
	        this.rows = rows;  
	    } 
}
